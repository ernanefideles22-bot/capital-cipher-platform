  CONNECTED: "connected", DISCONNECTED: "disconnected", READY: "ready", FAILED: "failed", TIMEOUT: "timeout", ACTIVE: "active", INACTIVE: "inactive", ENABLED: "enabled", DISABLED: "disabled", BUY: "buy", SELL: "sell", APPROVED: "approved", REDUCED: "reduced", BLOCKED: "blocked", PENDING: "pending", LIVE_LOCKED: "liveLocked",
