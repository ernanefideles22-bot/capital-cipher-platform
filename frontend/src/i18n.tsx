interface I18nValue { language: Language; setLanguage: (language: Language) => void; t: (key: keyof typeof PT) => string; status: (value: string) => string; }
const I18nContext = createContext<I18nValue | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>(() => {
    try {
      const saved = window.localStorage.getItem("capital-cipher-language");
      return saved === "en-US" ? "en-US" : "pt-BR";
    } catch {
      return "pt-BR";
    }
  });
  const setLanguage = (next: Language) => { setLanguageState(next); try { window.localStorage.setItem("capital-cipher-language", next); } catch { /* preference remains in memory */ } };
  useEffect(() => { document.documentElement.lang = language; }, [language]);
  const value = useMemo<I18nValue>(() => ({
    language,
    setLanguage,
    t: (key) => (language === "pt-BR" ? PT[key] : EN[key]),
    status: (value) => { const key = STATUS_KEYS[value]; return key ? (language === "pt-BR" ? PT[key] : EN[key]) : value; },
  }), [language]);
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nValue {
  const value = useContext(I18nContext);
  if (!value) throw new Error("useI18n must be used inside I18nProvider");
  return value;
}
